mod utils;

use compile_test::build_temp_files;

use self::utils::should_eq;

#[test]
fn test_node_modules() {
    let dir = build_temp_files(serde_json::json!({
    "./node_modules/a/dist/index.d.ts": "",
    "./node_modules/a/package.json": r#"{
        "name": "a",
        "types": "dist/index.d.ts"
}"#,
    "./index.ts": "",
    }));
    let dir = dir.path();
    let from = dir.join("./index.ts");
    let expected = dir.join("./node_modules/a/dist/index.d.ts");
    let expected = std::fs::canonicalize(expected).unwrap();
    should_eq(&from, "a", expected);
}
