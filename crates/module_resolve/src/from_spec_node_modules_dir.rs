pub struct Loader {
    rest_idx: Option<usize>,
    package_json: Option<PackageJsonPath>,
}

impl<'a, 'options, FS: bolt_ts_fs::CachedFileSystem> ResolutionKindSpecLoader<'a, 'options, FS>
    for Loader
{
    fn loader(
        &self,
        ext: Extensions,
        candidate: &mut std::path::PathBuf,
        only_record_failures: bool,
        state: &ModuleResolutionState<'_, '_, FS>,
        cache: &ModuleResolutionCache,
    ) -> Option<RResult<PathId>> {
        let path_and_extension = load_module_from_file(ext, candidate, only_record_failures, state)
            .or_else(|_| {
                load_node_module_from_directory_worker(
                    ext,
                    candidate,
                    only_record_failures,
                    state,
                    self.package_json,
                    cache,
                )
            });
        if path_and_extension.is_err()
            && self.rest_idx.is_none()
            && self.package_json.is_some_and(|_| false)
        {
            todo!()
        }
        Some(path_and_extension)
    }
}

pub(super) fn load_module_from_spec_node_modules_directory<'a, 'options, FS: CachedFileSystem>(
    exts: Extensions,
    module_name: Atom,
    node_module_dir: PathId,
    node_module_dir_exist: bool,
    state: &ModuleResolutionState<'a, 'options, FS>,
    cache: &ModuleResolutionCache,
) -> Option<RResult<PathId>> {
    let mut atoms = state.atoms.lock().unwrap();
    let dir = Path::new(atoms.get(node_module_dir.into()));
    debug_assert!(dir.is_normalized());
    let name = atoms.get(module_name);
    let rest_idx = super::parse_package_name::split_slash_for_package_name(name);
    let mut candidate = normalize_join(dir, name);
    debug_assert!(candidate.is_normalized());
    let bytes = candidate.as_os_str().as_encoded_bytes();
    let s = unsafe { std::str::from_utf8_unchecked(bytes) };
    let candidate_id = atoms.atom(s).into();
    // let pkg_dir = if let Some(rest_idx) = rest_idx {
    //     normalize_join(dir, &name[..rest_idx])
    // } else {
    //     normalize_join(dir, name)
    // };
    drop(atoms);
    let package_json = get_pkg_json_info(candidate_id, !node_module_dir_exist, state, cache);
    // if let Some(rest_idx) = rest_idx {
    //     todo!()
    // }
    if let Some(package_json) = package_json {
        state.resolved_package_directory.set(true);
        if state.features.contains(NodeResolutionFeatures::EXPORTS)
            && let json = cache.package_json_cache().read_package_json(package_json)
            && let Some(exports) = json.exports()
        {
            let subpath = if let Some(rest_idx) = rest_idx {
                Cow::Owned(combine_paths(".", &[&name[rest_idx..]]))
            } else {
                Cow::Borrowed(".")
            };
            // TODO: return load_module_from_exports(exports, exts, &subpath, state, cache);
        }
    }

    let loader = Loader {
        rest_idx,
        package_json,
    };
    loader.loader(exts, &mut candidate, !node_module_dir_exist, state, cache)
}

fn all_keys_start_with_dot(value: &serde_json::Value) -> bool {
    match value {
        serde_json::Value::Null | serde_json::Value::Bool(_) | serde_json::Value::Number(_) => {
            false
        }
        serde_json::Value::String(s) => s.starts_with("."),
        serde_json::Value::Array(array) => array.iter().all(|v| all_keys_start_with_dot(v)),
        serde_json::Value::Object(map) => map.keys().all(|k| k.starts_with(".")),
    }
}

fn no_key_start_with_dot(value: &serde_json::Value) -> bool {
    match value {
        serde_json::Value::Null | serde_json::Value::Bool(_) | serde_json::Value::Number(_) => true,
        serde_json::Value::String(s) => s.starts_with("."),
        serde_json::Value::Array(array) => array.iter().all(|v| no_key_start_with_dot(v)),
        serde_json::Value::Object(map) => map.keys().all(|k| !k.starts_with(".")),
    }
}

