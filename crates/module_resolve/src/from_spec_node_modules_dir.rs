fn load_module_from_string_target_export_or_import<
    'a,
    'options,
    const IS_IMPORT: bool,
    const PATTERN: bool,
    FS: CachedFileSystem,
>(
    exts: Extensions,
    package_json: PackageJsonPath,
    target: &str,
    subpath: &str,
    key: &str,
    state: &ModuleResolutionState<'a, 'options, FS>,
) -> Option<RResult<PathId>> {
    if !PATTERN && !subpath.is_empty() && !target.ends_with("/") {
        todo!();
    } else if !target.starts_with("./") {
        todo!();
    } else {
        debug_assert!(path_is_relative(target));
        if target
            .split(bolt_ts_utils::path::SLASH as char)
            .skip(1)
            .any(|part| part == ".." || part == "." || part == NODE_MODULES_FOLDER)
        {
            return None;
        }
        if subpath
            .split(bolt_ts_utils::path::SLASH as char)
            .any(|item| item == ".." || item == "." || item == NODE_MODULES_FOLDER)
        {
            return None;
        }
        let target = if PATTERN {
            // target.replace("*", subpath)
            todo!()
        } else {
            target
        };
        let base = state.atoms.lock().unwrap().get(package_json.path().into());
        debug_assert!(base.ends_with("/package.json"));
        let path = combine_paths(&base[..base.len() - "/package.json".len()], &[target]);
        let path = std::path::PathBuf::from(path);
        let mut path = path.normalize();
        // TODO: input_link, with_package_id
        load_filename_from_package_json_field(exts, &mut path, Some(package_json), false, state)
    }
}

fn load_module_from_map_target_export_or_import<
    'a,
    'options,
    const IS_IMPORT: bool,
    const PATTERN: bool,
    FS: CachedFileSystem,
>(
    exts: Extensions,
    package_json: PackageJsonPath,
    target: &serde_json::Map<String, serde_json::Value>,
    subpath: &str,
    key: &str,
    state: &ModuleResolutionState<'a, 'options, FS>,
) -> Option<RResult<PathId>> {
    for (condition, sub_target) in target {
        if condition == "default"
            || state.conditions.contains(condition)
            || is_applicable_versioned_types_key(&state.conditions, key)
        {
            if let Some(Ok(result)) = load_module_from_target_export_or_import::<
                IS_IMPORT,
                PATTERN,
                FS,
            >(
                exts, package_json, sub_target, subpath, key, state
            ) {
                return Some(Ok(result));
            }
        }
    }
    None
}

fn is_applicable_versioned_types_key(conditions: &[String], key: &str) -> bool {
    if !conditions.iter().any(|c| c == "types") || !key.starts_with("types@") {
        return false;
    }
    let sub = &key["types@".len()..];
    let Ok(range) = bolt_ts_semver::parse_range(sub) else {
        return false;
    };
    // TODO: version:
    const VERSION_MAJOR_MINOR: &str = "6.0";
    let version = format!("{}.0-dev", VERSION_MAJOR_MINOR);
    let version = bolt_ts_semver::parse_version(&version).unwrap();
    range.test(&version)
}
